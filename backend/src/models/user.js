// backend/src/models/user.js

const { DataTypes } = require('sequelize');
const db = require('../config/database'); // Adjust the path as per your project structure

const User = db.define('User', {
  username: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
    validate: {
      isEmail: true,
    },
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});

module.exports = User;
